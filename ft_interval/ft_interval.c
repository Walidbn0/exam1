#include <unistd.h>

int main (int argc, char **argv)
{
	int i;
	int j;
	i = 0;
	j = 1;
	if (argc != 1)
	{
		while (argv[1][i] != '\0')
	{
			if (j % 5 == 0 && j % 3 == 0)
			{
				write (1, "5", 1);
			}
			else if (j % 3 == 0 )
				write (1, "5", 1);
			else if (j % 5 == 0)
				write(1, "3", 1);
			else
			write (1, &argv[1][i], 1);
			j++;
			i++;
		}
	}
	return (0);
}
